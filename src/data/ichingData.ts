// Returns a full Hexagram object with King Wen Number mapped
export function getHexagramByBinary(binary: number[]): Hexagram & { element: string } {
  // Find match
  const match = Object.entries(HEXAGRAMS_DB).find(([_, data]) => {
    return data.binary.every((val, index) => val === binary[index]);
  });

  const num = match ? parseInt(match[0], 10) : 1;
  const data = HEXAGRAMS_DB[num] || HEXAGRAMS_DB[1];
  
  // Determine element from upper trigram
  const upperTri = TRIGRAMS[data.upperTrigram] || TRIGRAMS.qian;
  
  return {
    number: num,
    ...data,
    element: upperTri.element,
  };
}

